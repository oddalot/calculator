<script>
export default {
  name: 'calculator',
  data() {
    return {
      stringNum: '',
      stringNumNotFull() {
        if (this.stringNum.indexOf('.') === -1) {
          return (this.stringNum.length) !== 7;
        }
        return this.stringNum.length - 1 !== 7;
      },
      stack: [],
      eightLit: ['', '', '', '', '', '', ''],
      decimalLit: [false, false, false, false, false, false, false],
      numberToString: {
        0: 'zero',
        1: 'one',
        2: 'two',
        3: 'three',
        4: 'four',
        5: 'five',
        6: 'six',
        7: 'seven',
        8: 'eight',
        9: 'nine',
      },
    };
  },
  methods: {
    ceButton() {
      this.stringNum = '';
      this.updateScreen();
    },
    acButton() {
      this.stringNum = '';
      this.stack = [];
      this.updateScreen();
    },
    numberButton(num) {
      if (this.stringNumNotFull()) {
        if (num === '.' && this.stringNum.indexOf('.') === -1) {
          this.stringNum += '.';
          this.updateScreen();
        } else {
          this.stringNum += num;
          this.updateScreen();
        }
      }
    },
    operatorButton(operator) {
      this.stack.push(Number(this.stringNum));
      this.stringNum = '';
      this.stack.push(operator);
    },
    equalsButton() {
      this.stack.push(Number(this.stringNum));
      this.stringNum = this.calculateStack().toString();
      this.updateScreen();
    },
    calculateStack() {
      let tempVal;
      while (this.stack.length > 0) {
        const value = this.stack.shift();
        switch (value) {
          case '/':
            tempVal /= this.stack.shift();
            break;
          case '*':
            tempVal *= this.stack.shift();
            break;
          case '+':
            tempVal += this.stack.shift();
            break;
          case '-':
            tempVal -= this.stack.shift();
            break;
          default:
            tempVal = value;
            break;
        }
      }
      return tempVal;
    },
    clearScreen() {
      this.eightLit = ['', '', '', '', '', '', ''];
      this.decimalLit = [false, false, false, false, false, false, false];
    },
    updateScreen() {
      let eightNumber = 0;
      const workingNumArr = this.stringNum.split('');
      this.clearScreen();
      while (workingNumArr.length > 0) {
        let workingDigit = workingNumArr.pop();
        if (workingDigit === '.') {
          this.turnOnEight(eightNumber, workingDigit);
          workingDigit = workingNumArr.pop();
        }
        this.turnOnEight(eightNumber, workingDigit);
        eightNumber += 1;
      }
    },
    turnOnEight(eightNumber, workingDigit) {
      if (workingDigit === '.') {
        this.decimalLit[eightNumber] = true;
      } else {
        this.eightLit[eightNumber] = this.numberToString[workingDigit];
      }
    },
    setOneDecimal() {
      this.decimal0 = 1;
    },
  },
};
